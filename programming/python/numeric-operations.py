# Integer variables
n1 = 10
n2 = 5

# Integer Division
r1 = n1 / n2
print("Integer Division:", r1)

# Modulus (Remainder)
r2 = n1 % n2
print("Modulus (Remainder):", r2)

# Absolute Value
r3 = abs(-7)
print("Absolute Value:", r3)


# Float variables
num1 = 5.0
num2 = 2.5

# Basic Arithmetic
result1 = num1 + num2
print("Addition:", result1)

result2 = num1 - num2
print("Subtraction:", result2)

result3 = num1 * num2
print("Multiplication:", result3)

result4 = num1 / num2
print("Division:", result4)

# Rounding
result5 = round(3.14159265359, 2)  # Rounds to 2 decimal places
print("Rounded:", result5)
